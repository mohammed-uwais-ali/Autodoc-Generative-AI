# Function: x
#
# Parameters:
# - a: The input value
#
# Returns:
# - The square of the input value
def x(a):

    return a * a


# Function: y
#
# Parameters:
# - a: The first input value
# - b: The second input value
#
# Returns:
# - The sum of the input values
def y(a, b):

    return a + b


# Function: z
#
# Parameters:
# - a: The first input value
# - b: The second input value
#
# Returns:
# - The difference between the first and second input values
def z(a, b):

    return a - b
